package com.adviteya.model;

import org.slim3.datastore.Model;

/**
 * @author Dheeraj
 * 
 */
@Model(schemaVersion = 1)
public class MA_UserProfile extends AbstractEntity
{
	
	/**
     * 
     */
	private static final long serialVersionUID = -4353116306145207019L;
	
	private String            profileType;
	
	private String            profileId;
	
	private String            userProfileDesc;
	
	/**
	 * @return the userProfileDesc
	 */
	public String getUserProfileDesc()
	{
		return userProfileDesc;
	}
	
	/**
	 * @param userProfileDesc
	 *            the userProfileDesc to set
	 */
	public void setUserProfileDesc(String userProfileDesc)
	{
		this.userProfileDesc = userProfileDesc;
	}
	
	public String getProfileId()
	{
		return profileId;
	}
	
	public void setProfileId(String profileId)
	{
		this.profileId = profileId;
	}
	
	public String getProfileType()
	{
		return profileType;
	}
	
	public void setProfileType(String profileType)
	{
		this.profileType = profileType;
	}
	
}
